package ec.hcam.data;

import java.io.Serializable;

public class GasesMedicinales implements Serializable {

    private String tipo;
    private int identificador;
    private String unidadMedica;
    private String historiaClinica;
    private int minutos;
    private double total;
    private String fechaCreacion;
    private String horaCreacion;
    private double litrosMinuto;
    private int tiempo;
    private int parcial;
    private String codigo;

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the identificador
     */
    public int getIdentificador() {
        return identificador;
    }

    /**
     * @param identificador the identificador to set
     */
    public void setIdentificador(int identificador) {
        this.identificador = identificador;
    }

    /**
     * @return the unidadMedica
     */
    public String getUnidadMedica() {
        return unidadMedica;
    }

    /**
     * @param unidadMedica the unidadMedica to set
     */
    public void setUnidadMedica(String unidadMedica) {
        this.unidadMedica = unidadMedica;
    }

    /**
     * @return the historiaClinica
     */
    public String getHistoriaClinica() {
        return historiaClinica;
    }

    /**
     * @param historiaClinica the historiaClinica to set
     */
    public void setHistoriaClinica(String historiaClinica) {
        this.historiaClinica = historiaClinica;
    }

    /**
     * @return the minutos
     */
    public int getMinutos() {
        return minutos;
    }

    /**
     * @param minutos the minutos to set
     */
    public void setMinutos(int minutos) {
        this.minutos = minutos;
    }

    /**
     * @return the total
     */
    public double getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(double total) {
        this.total = total;
    }

    /**
     * @return the fechaCreacion
     */
    public String getFechaCreacion() {
        return fechaCreacion;
    }

    /**
     * @param fechaCreacion the fechaCreacion to set
     */
    public void setFechaCreacion(String fechaCreacion) {
        this.fechaCreacion = fechaCreacion;
    }

    /**
     * @return the horaCreacion
     */
    public String getHoraCreacion() {
        return horaCreacion;
    }

    /**
     * @param horaCreacion the horaCreacion to set
     */
    public void setHoraCreacion(String horaCreacion) {
        this.horaCreacion = horaCreacion;
    }

    /**
     * @return the litrosMinuto
     */
    public double getLitrosMinuto() {
        return litrosMinuto;
    }

    /**
     * @param litrosMinuto the litrosMinuto to set
     */
    public void setLitrosMinuto(double litrosMinuto) {
        this.litrosMinuto = litrosMinuto;
    }

    /**
     * @return the tiempo
     */
    public int getTiempo() {
        return tiempo;
    }

    /**
     * @param tiempo the tiempo to set
     */
    public void setTiempo(int tiempo) {
        this.tiempo = tiempo;
    }

    /**
     * @return the parcial
     */
    public int getParcial() {
        return parcial;
    }

    /**
     * @param parcial the parcial to set
     */
    public void setParcial(int parcial) {
        this.parcial = parcial;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

}
