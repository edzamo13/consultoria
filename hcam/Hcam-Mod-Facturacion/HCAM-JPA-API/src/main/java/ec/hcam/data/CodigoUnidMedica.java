package ec.hcam.data;

import java.io.Serializable;

/**
 *
 * @author programacion
 */
public class CodigoUnidMedica implements Serializable{
    
    private int CTUMIDPK;
    private int IDUNIDMED;
    private String CODIGO;
    private int NIVEL;
    private int IDUBICA;

    public int getCTUMIDPK() {
        return CTUMIDPK;
    }

    public void setCTUMIDPK(int CTUMIDPK) {
        this.CTUMIDPK = CTUMIDPK;
    }

    public int getIDUNIDMED() {
        return IDUNIDMED;
    }

    public void setIDUNIDMED(int IDUNIDMED) {
        this.IDUNIDMED = IDUNIDMED;
    }

    public String getCODIGO() {
        return CODIGO;
    }

    public void setCODIGO(String CODIGO) {
        this.CODIGO = CODIGO;
    }

    public int getNIVEL() {
        return NIVEL;
    }

    public void setNIVEL(int NIVEL) {
        this.NIVEL = NIVEL;
    }

    public int getIDUBICA() {
        return IDUBICA;
    }

    public void setIDUBICA(int IDUBICA) {
        this.IDUBICA = IDUBICA;
    }
    
    
}
